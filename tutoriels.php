        <h1>Mon tutoriel</h1>
        <h2>Comment créer une feuille de style?</h2>
        <div>
        <p>
        w3c: <br>
le tuto explique comment créer un fichier HTML, un fichier CSS et les faire fonctionner ensemble. <br>
Il commence par nous conseiller les outils disponibles, ensuite les bonnes pratiques pour ne pas se perdre dans 
la réalisation des différentes tâches.
il poursuit en expliquant les différentes lignes du fichier HTML, donnant ainsi plus de détail sur chaque  
élément de base du fichier.

 Ensuite il explique comment ajouter la feuille de style CSS sur un fichier HTML,en donnant immediatement
le rendu pour nous permettre de comprendre le rôle des commandes que nous manipulons.

je trouve ce tuto très intéressant et bien détaillé, 
il est simple à comprendre, une bonne curiosité pour se lancer à la réalisation de son premier site. <br><br>

<a href="https://www.w3.org/Style/Examples/011/firstcss" target="_blank">
                
                https://www.w3.org/Style/Examples/011/firstcss</a>



        </p>
        </div>

